document.addEventListener('DOMContentLoaded', function () {
  const quizList = document.getElementById('quizList');
  if (quizList) {
    // Генерація списку вікторин
    quizzes.forEach(function (quiz, index) {
      const listItem = document.createElement('li');
      const link = document.createElement('a');
      link.href = 'play.html?quiz=' + index;
      link.textContent = quiz.title;

      const questionCount = document.createElement('span');
      questionCount.textContent = '(' + quiz.questions.length + ' питань)';
      questionCount.classList.add('question-count');

      listItem.appendChild(link);
      listItem.appendChild(questionCount);
      quizList.appendChild(listItem);
    });
  }
});

function getRandomQuiz() {
  const randomIndex = Math.floor(Math.random() * quizzes.length);
  const randomQuiz = quizzes[randomIndex];
  return randomQuiz;
}

const luckyButton = document.getElementById('luckyButton');
if (luckyButton) {
  luckyButton.addEventListener('click', function () {
    const randomQuiz = getRandomQuiz();
    window.location.href = 'play.html?quiz=' + quizzes.indexOf(randomQuiz);
  });
}